Djelomično naslonjen na futurizam, koji u sebi nosi dinamiku nošenu objektom i prostorom, Nikola koristi simultano kretanje na plohi kroz geometrizaciju pojedinih elemenata, fragmentira i dekomponira plohu i volumen, te lomi na sastavne djelove i ponovno spaja naizgled nespojivo.
Koristeći tradicionalne grafičke tehnike kao što su sitotisak, litografija, linorez i tuš, samostalno ili u kombinaciji, on istražuje mogućnosti koje one daju stvarajući futurističke kompozicije i tako istražuje i ukazuje na zastrašujuću stvarnost suvremenog doba u kojem živimo.
Otuđenje kao posljedica modernizacije zapravo stvara mehaničkog čovjeka lišenog emocija i osuđenog na uvjetovani pokret i misao. 
Njegovog čovjeka pokreće mehanički sustav sitnih i krupnih elemenata u složenom međuodnosu koji ne podnosi ni najmanju nepreciznost u prijenosu energije gdje svaka greška može prouzročiti katastrofalne posljedice.
Bilo da se bavi portretom, figurom ili ističe samo detalj složenog sustava, Nikola progovara kroz kontrast crne i bijele ili modelira pomoću tonaliteta sive. Namjerno izostavlja boju kao element koja bi na neki način sugerirala život, ostavljajući tako promatraču da sliku prema potrebi provuče kroz osobni spektar. 
